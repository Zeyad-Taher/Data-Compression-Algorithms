    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        File file = new File("D:\\Java\\Huffman\\src\\huffman\\data.txt");
        try {
            Huffman.compress(file);
            String dictionary="Character    Code\n";
            for(int i=0;i<Huffman.dictionaryChar.size();i++){
                dictionary+="       "+Huffman.dictionaryChar.get(i)+"            "+Huffman.dictionaryCode.get(i)+"\n";   
            }
            jTextArea1.setText(dictionary);
            jTextArea1.setEditable(false);
            String compressedData=Files.readAllLines(Paths.get("D:\\Java\\Huffman\\src\\huffman\\compressed.txt")).get(3);
            jTextArea4.setText(compressedData);
            jTextArea4.setLineWrap(true);
            jTextArea4.setWrapStyleWord(true);
            jTextArea4.setEditable(false);
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        double ratio=(double)Huffman.uncompressedSize/(double)Huffman.compressedSize;
        jTextArea2.setText("Size before compression = "+Huffman.uncompressedSize+" Bits"+"\n"+"Size after compression = "+Huffman.compressedSize+" Bits\n"
        +"Compression ratio = "+String.format("%.3f", ratio));
        System.out.println("Size before compression = "+Huffman.uncompressedSize+" Bits"+"\n"+"Size after compression = "+Huffman.compressedSize+" Bits\n"
        +"Compression ratio = "+String.format("%.3f", ratio));
        jTextArea2.setEditable(false);
    }//GEN-LAST:event_jButton1ActionPerformed

